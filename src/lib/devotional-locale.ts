export function getLocalizedDevotionalLines(
  item: DevotionalItem,
  lang: LangCode,
): string[] {
  const locale = contentLocaleFor(lang);
  if (locale === "en") return item.linesEn;
  if (locale === "hi") return item.linesHi;

  const override = LOCALE_LINES[item.id]?.[locale];
  if (override?.length) return override;

  // Never force Devanagari on regional scripts — use English transliteration as fallback.
  return item.linesEn;
}
